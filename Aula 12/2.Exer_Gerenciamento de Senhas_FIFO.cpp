/*Exercicio Proposto

Implementar um sistema básico de gerenciamento 
de senhas que funcione de acordo com o princípio 
FIFO.Este sistema deve ser capaz de gerar novas 
senhas, chamar o próximo cliente para atendimento 
e exibir a fila de clientes aguardando.*/

/* Funcionalidades:

1.Gerar Nova Senha (Enfileirar):
Número sequencial automático gerenciado pelo 
programa.Esta senha deve ser adicionada ao final 
da fila de atendimento.Exiba uma mensagem confirmando 
a geração e o número da senha.

2.Chamar Próximo Cliente (Desenfileirar):
Esta opção simula o atendimento de um cliente. 
O primeiro elemento cliente deve ser removido da fila.
Exiba uma mensagem informando qual senha foi chamada para 
atendimento.Caso a fila esteja vazia, exiba uma mensagem. 

3.Visualizar Próximo Cliente (Front):
Permite visualizar qual é a próxima senha a ser chamada, 
sem removê-la da fila.
Exiba a senha do cliente que está no início da fila.
Caso a fila esteja vazia, exiba uma mensagem

4.Exibir Fila de Atendimento:
Mostra todas as senhas que estão atualmente aguardando 
na fila, da primeira à última.
Exiba uma mensagem se a fila estiver vazia.

5. Exibir Último da Fila

6. Exibir Nmro de Clientes

7. Exibir as Senhas já atendidas

8.Sair:
Encerra o programa, garantindo que toda a memória alocada 
para a fila seja liberada.
*/

